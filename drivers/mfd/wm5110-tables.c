/*                                              */
int wm5110_patch(struct arizona *arizona)
{
	switch (arizona->rev) {
	case 0:
		return regmap_register_patch(arizona->regmap,
					     wm5110_reva_patch,
					     ARRAY_SIZE(wm5110_reva_patch));
	case 1:
		return regmap_register_patch(arizona->regmap,
					     wm5110_revb_patch,
					     ARRAY_SIZE(wm5110_revb_patch));
	case 3:
		return regmap_register_patch(arizona->regmap,
					     wm5110_revd_patch,
					     ARRAY_SIZE(wm5110_revd_patch));
	default:
		return 0;
	}
}
EXPORT_SYMBOL_GPL(wm5110_patch);

